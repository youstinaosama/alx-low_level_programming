/**
 * swap_int - change the value
 * @a: first value
 * @b: second value
 *
 */
void swap_int(int *a, int *b)
{
	int x;
	int y;

       if (x < y)
       {	       
		*a ==&y;
		*b ==&x;
       }
       else
       {
	       *a == &x;
	       *b == &y;
       }

}
